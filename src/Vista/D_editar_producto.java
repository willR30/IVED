    private void btn_entrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrar1ActionPerformed
        // TODO add your handling code here:
        //editar un producto
         if(this.txt_nombre.getText().isEmpty() || this.txt_descripcion.getText().isEmpty() || this.txt_codigo.getText().isEmpty()||txt_codigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe completar todos los campos");
        }else{
           if(Integer.parseInt(this.txt_cantidad.getText())<=0 ||this.txt_precio.getText().isEmpty() ||Double.parseDouble(this.txt_precio.getText())<=0){
               JOptionPane.showMessageDialog(null,"No se admiten campos negativos  y Valor Cero");
           }else{
               //capturamos el id de la marca para pasarlo al método
               Ctr_marcas cm=new Ctr_marcas();
               int id_marca=cm.ID_marcca(this.combo_marcas.getSelectedItem().toString().trim());
               Ctr_productos ctr=new Ctr_productos();
               //capturamos el precio del producto
               float precio=Float.parseFloat(this.txt_precio.getText().trim());
               ctr.actualizar_producto(this.Codigo_identificador,this.txt_nombre.getText(),id_marca, txt_descripcion.getText(), txt_codigo.getText(),precio);
               this.hide();
           }
        }
    }//GEN-LAST:event_btn_entrar1ActionPerformed

